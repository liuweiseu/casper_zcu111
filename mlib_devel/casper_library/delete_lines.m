function delete_lines(cursys)
% Remove all lines from a system.
%
% delete_lines(cursys)

try
    lines = get_param(cursys, 'Lines');
    for k=1:length(lines),
        delete_line(lines(k).Handle);
    end
catch ex
    dump_and_rethrow(ex);
end
